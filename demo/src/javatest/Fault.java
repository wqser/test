public class Fault   {



    private String reportTime;

    private String faultName;

    private String faultCode;

    private String faultNum;

    private String faultTime;

    private String soluteStatus;

    private String soluteTime;



    public String getReportTime() {
        return reportTime;
    }

    public void setReportTime(String reportTime) {
        this.reportTime = reportTime;
    }

    public String getFaultName() {
        return faultName;
    }

    public void setFaultName(String faultName) {
        this.faultName = faultName;
    }

    public String getFaultCode() {
        return faultCode;
    }

    public void setFaultCode(String faultCode) {
        this.faultCode = faultCode;
    }

    public String getFaultNum() {
        return faultNum;
    }

    public void setFaultNum(String faultNum) {
        this.faultNum = faultNum;
    }

    public String getFaultTime() {
        return faultTime;
    }

    public void setFaultTime(String faultTime) {
        this.faultTime = faultTime;
    }

    public String getSoluteStatus() {
        return soluteStatus;
    }

    public void setSoluteStatus(String soluteStatus) {
        this.soluteStatus = soluteStatus;
    }

    public String getSoluteTime() {
        return soluteTime;
    }

    public void setSoluteTime(String soluteTime) {
        this.soluteTime = soluteTime;
    }
}
